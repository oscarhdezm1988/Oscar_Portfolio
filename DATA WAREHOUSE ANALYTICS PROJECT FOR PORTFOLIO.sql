--8.Data Segmentation 
--8.1.Segment products into cost ranges and count how many products fall into each segment 

WITH Cost_Range_Product AS (
SELECT 
product_name,
cost,
CASE
WHEN cost < 100 THEN 'Below 100'
WHEN cost BETWEEN 100 AND 500 THEN '100-500'
WHEN cost BETWEEN 500 AND 1000 THEN '500-1000'
ELSE 'Above 1000'
END AS Cost_Range
FROM DataWarehouseAnalytics.gold.dim_products
)

SELECT
Cost_Range,
COUNT(DISTINCT (product_name)) AS Ranges_Count
FROM Cost_Range_Product 
GROUP BY Cost_Range
ORDER BY Ranges_Count DESC

--8.1.Group customers into three segments based on their spending behavior:
--VIP: Customers with at least 12 months of history and spending more than €5,000.
--Regular: Customers with at least 12 months of history but spending €5,000 or less.
--New: Customers with a lifespan less than 12 months.
--Find the total number of customers by each group

WITH customer_spending AS (
    SELECT
        c.customer_key,
        SUM(f.sales_amount) AS total_spending,
        MIN(order_date) AS first_order,
        MAX(order_date) AS last_order,
        DATEDIFF(month, MIN(order_date), MAX(order_date)) AS lifespan
    FROM gold.fact_sales f
    LEFT JOIN gold.dim_customers c
        ON f.customer_key = c.customer_key
    GROUP BY c.customer_key
)
SELECT 
    customer_segment,
    COUNT(customer_key) AS total_customers
FROM (
    SELECT 
        customer_key,
        CASE 
            WHEN lifespan >= 12 AND total_spending > 5000 THEN 'VIP'
            WHEN lifespan >= 12 AND total_spending <= 5000 THEN 'Regular'
            ELSE 'New'
        END AS customer_segment
    FROM customer_spending
) AS segmented_customers
GROUP BY customer_segment
ORDER BY total_customers DESC;













