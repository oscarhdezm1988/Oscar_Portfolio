--6.Cumulative Analysis 
--6.1.Find the total sales per month and the running total of sales over time per year

SELECT
Order_Month,
Total_Sales_by_Date,
SUM(Total_Sales_by_Date) OVER (PARTITION BY YEAR (Order_Month) ORDER BY Order_Month) AS Running_Total_Sales_by_Year,
AVG_price,
AVG(avg_price) OVER (PARTITION BY YEAR (Order_Month) ORDER BY Order_Month) AS Moving_AVG_Price_by_Year
FROM (
	SELECT
	DATETRUNC(month, order_date) AS Order_Month,
	SUM(sales_amount) AS Total_Sales_by_Date,
	AVG(price) AS AVG_price
	FROM DataWarehouseAnalytics.gold.fact_sales
	WHERE order_date IS NOT NULL
	GROUP BY DATETRUNC(month, order_date))t


--7.Performance Analysis 
--7.1.Analyze the yearly performance of products by comparing each product's sales to its
--average sales performance and the previous year's sales

WITH yearly_product_sales AS (
SELECT 
YEAR(S.order_date) AS Order_Year,
P.product_name,
SUM(S.sales_amount) AS Total_Product_Sales
FROM DataWarehouseAnalytics.gold.fact_sales AS S
LEFT JOIN DataWarehouseAnalytics.gold.dim_products AS P
ON S.product_key = P.product_key
WHERE S.order_date IS NOT NULL
GROUP BY
YEAR(S.order_date),
P.product_name
)

SELECT 
Order_Year,
product_name,
Total_Product_Sales,
AVG(Total_Product_Sales) OVER(PARTITION BY product_name) AS AVG_Product_Sales,
Total_Product_Sales - AVG(Total_Product_Sales) OVER(PARTITION BY product_name) AS Difference_AVG,
CASE
WHEN Total_Product_Sales - AVG(Total_Product_Sales) OVER(PARTITION BY product_name) > 0 THEN 'Above AVG'
WHEN Total_Product_Sales - AVG(Total_Product_Sales) OVER(PARTITION BY product_name) < 0 THEN 'Below AVG' 
ELSE 'AVG'
END AS Product_Performance,
LAG(Total_Product_Sales) OVER (PARTITION BY product_name ORDER BY Order_Year) AS TotalSales_Performance_by_Previous_Year,
Total_Product_Sales - LAG(Total_Product_Sales) OVER (PARTITION BY product_name ORDER BY Order_Year) AS Difference_with_Previous_Year,
CASE
WHEN Total_Product_Sales - LAG(Total_Product_Sales) OVER (PARTITION BY product_name ORDER BY Order_Year) > 0 THEN 'Increase'
WHEN Total_Product_Sales - LAG(Total_Product_Sales) OVER (PARTITION BY product_name ORDER BY Order_Year) < 0 THEN 'Decrease' 
ELSE 'No Change'
END AS Previous_Year_Chage_Evaluation
FROM yearly_product_sales
ORDER BY product_name, Order_Year

